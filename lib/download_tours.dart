Future download(int id) async {
  final File file = File('tours/$id.json');

  if (!file.existsSync()) {
    final String url =
        'https://www.komoot.com/api/v007/smart_tours/$id?format=v2&_embedded=coordinates%2Csurfaces%2Cway_types%2Cdirections&hl=en';

    final response = await http.get(url);

    if (response.statusCode == 200) {
      await file.writeAsString(response.body);
    } else {
      print('ERROR $id - ${response.statusCode}');
    }
  }
}
